package com.group01.aurora_demo.catalog.model;

import java.time.LocalDate;
import java.util.List;

public class Product {

    private Long productId;
    private Long shopId;
    private String sku;
    private String title;
    private String shortDescription;
    private String description;
    private Double price;
    private Double discount;
    private Integer soldCount;
    private Integer stock;
    private Long categoryId;
    private String publisher;
    private LocalDate publishedDate;
    private BookDetail bookDetail;
    private String primaryImageUrl;
    private List<ProductImages> images;

    public Product() {
    }

    public String getShortDescription() {
        return shortDescription;
    }

    public void setShortDescription(String shortDescription) {
        this.shortDescription = shortDescription;
    }

    public Long getProductId() {
        return productId;
    }

    public void setProductId(Long productId) {
        this.productId = productId;
    }

    public Long getShopId() {
        return shopId;
    }

    public void setShopId(Long shopId) {
        this.shopId = shopId;
    }

    public String getSku() {
        return sku;
    }

    public void setSku(String sku) {
        this.sku = sku;
    }

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    public String getDescription() {
        return description;
    }

    public void setDescription(String description) {
        this.description = description;
    }

    public Double getPrice() {
        return price;
    }

    public void setPrice(Double price) {
        this.price = price;
    }

    public Double getDiscount() {
        return discount;
    }

    public void setDiscount(Double discount) {
        this.discount = discount;
    }

    public Integer getSoldCount() {
        return soldCount;
    }

    public void setSoldCount(Integer soldCount) {
        this.soldCount = soldCount;
    }

    public Integer getStock() {
        return stock;
    }

    public void setStock(Integer stock) {
        this.stock = stock;
    }

    public Long getCategoryId() {
        return categoryId;
    }

    public void setCategoryId(Long categoryId) {
        this.categoryId = categoryId;
    }

    public String getPublisher() {
        return publisher;
    }

    public void setPublisher(String publisher) {
        this.publisher = publisher;
    }

    public LocalDate getPublishedDate() {
        return publishedDate;
    }

    public void setPublishedDate(LocalDate publishedDate) {
        this.publishedDate = publishedDate;
    }

    public BookDetail getBookDetail() {
        return bookDetail;
    }

    public void setBookDetail(BookDetail bookDetail) {
        this.bookDetail = bookDetail;
    }

    public String getPrimaryImageUrl() {
        return primaryImageUrl;
    }

    public void setPrimaryImageUrl(String primaryImageUrl) {
        this.primaryImageUrl = primaryImageUrl;
    }

    public List<ProductImages> getImages() {
        return images;
    }

    public void setImages(List<ProductImages> images) {
        this.images = images;
    }

    @Override
    public String toString() {
        return "Product{" + "productId=" + productId + ", shopId=" + shopId + ", sku=" + sku + ", title=" + title
                + ", shortDescription=" + shortDescription + ", description=" + description + ", price=" + price
                + ", discount=" + discount + ", soldCount=" + soldCount + ", stock=" + stock + ", categoryId="
                + categoryId + ", publisher=" + publisher + ", publishedDate=" + publishedDate + ", bookDetail="
                + bookDetail + ", images=" + images + '}';
    }
}